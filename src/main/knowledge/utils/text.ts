/**
 * 法律文档文本清理工具
 * 用于在分块前对原始文本进行预处理，提高检索精度
 */
export function legalCleanString(text: string): string {
  if (!text) return ''

  const cleaned = text
    // 1. 规范化换行符
    .replace(/\r\n/g, '\n')
    .replace(/\r/g, '\n')
    // 2. 移除重复的空白字符（保留单个换行或双换行作为语义分隔）
    .replace(/[ \t]+/g, ' ')
    // 3. 规范化中文标点符号（统一使用全角，符合中文法律文档习惯）
    .replace(/,/g, '，')
    .replace(/:/g, '：')
    .replace(/;/g, '；')
    .replace(/!/g, '！')
    .replace(/\?/g, '？')
    // 4. 处理法律条文中的常见格式（例如：第一条、 1. 、（一））
    // 识别带有空格的标题并规范化，例如 "第  一  条" -> "第一条"
    .replace(/(第)\s*([一二三四五六七八九十百千万]+)\s*([条章节])/g, '$1$2$3')
    // 移除条文编号前的多余空格，确保分块器能准确识别开头
    .replace(/\n\s+(第[一二三四五六七八九十]+[条章节])/g, '\n$1')
    .replace(/\n\s+(\d+\.)/g, '\n$1')
    // 处理行中出现的明显是标题但未换行的情况（可选，需谨慎）
    // 为了防止误伤，我们只在“句号/分号+空格+第X条”的情况下尝试换行
    .replace(/([。；])\s*(第[一二三四五六七八九十]+[条章节])/g, '$1\n$2')
    // 5. 移除 PDF 解析可能产生的特殊控制字符
    .replace(/[\x00-\x08\x0B\x0C\x0E-\x1F\x7F]/g, '')
    // 6. 确保段落之间有清晰的分隔
    .replace(/\n{3,}/g, '\n\n')
    .trim()

  return cleaned
}
